package Loops;

public class SwitchCase {

	public static void main(String[] args) {
		
		char grade='B';
		
		switch(grade) 
		{
		case 'A':
			System.out.println("Best perfomer");
			break;
		case 'B':
			System.out.println("Good perfomer");
			break;
		case 'C':
			System.out.println("Needs improvement");
			break;
		default:
			System.out.println("Invalid grade");
		
		
		
		}
		
		

	}

}
